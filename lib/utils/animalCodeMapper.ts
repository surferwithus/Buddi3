export function getAnimalKindName(code: string): string {
    if (code.includes("[개]")) {
        const breedPart = code.split("]")[1]?.trim();
        return breedPart || "믹스견";
    }

    if (code.includes("[고양이]")) {
        const breedPart = code.split("]")[1]?.trim();
        return breedPart || "믹스묘";
    }

    if (/^\d+$/.test(code)) {
        return animalKindMapper[code] || "기타";
    }

    return code || "알 수 없음";
}

export function getAnimalType(kindCd: string): "강아지" | "고양이" | "기타" {
    if (kindCd.includes("[개]") || kindCd.includes("견")) {
        return "강아지";
    }
    if (kindCd.includes("[고양이]") || kindCd.includes("묘")) {
        return "고양이";
    }

    const codeNum = parseInt(kindCd);
    if (!isNaN(codeNum)) {
        if (codeNum >= 400000 && codeNum < 420000) {
            return "강아지";
        }
        if (codeNum >= 420000 && codeNum < 430000) {
            return "고양이";
        }
    }

    return "기타";
}
